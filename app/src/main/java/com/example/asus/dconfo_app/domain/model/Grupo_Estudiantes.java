package com.example.asus.dconfo_app.domain.model;

public class Grupo_Estudiantes {
    private Integer idGrupoEstudiantes;
    private String nameGrupoEstudiantes;
    private Integer idDocente;
    private Integer idGrupo;

    public Integer getIdGrupoEstudiantes() {
        return idGrupoEstudiantes;
    }

    public void setIdGrupoEstudiantes(Integer idGrupoEstudiantes) {
        this.idGrupoEstudiantes = idGrupoEstudiantes;
    }

    public String getNameGrupoEstudiantes() {
        return nameGrupoEstudiantes;
    }

    public void setNameGrupoEstudiantes(String nameGrupoEstudiantes) {
        this.nameGrupoEstudiantes = nameGrupoEstudiantes;
    }

    public Integer getIdDocente() {
        return idDocente;
    }

    public void setIdDocente(Integer idDocente) {
        this.idDocente = idDocente;
    }

    public Integer getIdGrupo() {
        return idGrupo;
    }

    public void setIdGrupo(Integer idGrupo) {
        this.idGrupo = idGrupo;
    }
}
